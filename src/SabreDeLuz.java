
public class SabreDeLuz {
	private String cor;
	private String punho;
	
	public SabreDeLuz(String cor, String punho) {
		super();
		this.cor = cor;
		this.punho = punho;
	}
	
	public String getCor() {
		return cor;
	}
	public void setCor(String cor) {
		this.cor = cor;
	}
	public String getPunho() {
		return punho;
	}
	public void setPunho(String punho) {
		this.punho = punho;
	}
}
